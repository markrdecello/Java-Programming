public class DataSizes{
	public static void main(String[] args){
		System.out.println("An integer is " + Integer.SIZE + " bits");
		System.out.println("A float is " + Float.SIZE + " bits");
		System.out.println("A double is " + Double.SIZE + " bits");
		System.out.println("A short is " + Short.SIZE + " bits");
		System.out.println("A long is " + Long.SIZE + " bits");
		System.out.println("A byte is " + Byte.SIZE + " bit");
		System.out.println("A char is " + Character.SIZE + " bits");
	}
}
